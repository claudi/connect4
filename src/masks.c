static void printMask(const Mask mask) {
    for(short iter_i = (BOARD_SIZE - 1); iter_i >= 0; iter_i--) {
        for(short iter_j = 0; iter_j < BOARD_SIZE; iter_j++) {
            printf("%c", (mask & shift(iter_i, iter_j)) ? '1' : '0');
        }
        printf("\n");
    }
    printf("\n");
}

static void __attribute__((unused)) printMasks() {
    for(Matches iter = 0; iter < NMATCHES; iter++) {
        for(unsigned length = 0; length < nmasks[iter]; length++) {
            printMask(masks[iter][length].main);
        }
    }
}

static Bool matchMask(const Board board, const Mask mask) {
    return ((board & mask) == mask);
}

static Bool matchSMask(const Board *board, const SMask mask) {
    return matchMask(board[TURN], mask.main) && matchMask(board[BOTH], mask.anti);
}

unsigned matches(const Board *board, const unsigned length) {
    ASSERT(length < NMATCHES);

    unsigned count = 0;
    for(unsigned mask = 0; mask < nmasks[length]; mask++) {
        count += matchSMask(board, masks[length][mask]);
    }
    return count;
}

Mask shift(const short row, const short col) {
    return (((Mask) 1) << POS_TO_SHIFT(row, col));
}

